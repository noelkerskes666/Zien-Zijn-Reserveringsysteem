        <div> <h3> Wat is Mindfulness? </h3>
            <p> Aandacht is de essentie van mindfulness. Mindful zijn betekent dat we aandacht geven aan onze fysieke en
                zintuiglijke gewaarwordingen, ons bewust zijn van onze gedachtestroom en onze emotionele sensaties.
                Door aandacht te geven aan het hier en nu, zonder oordeel en met een vriendelijke, milde houding, laten we toe
                wat er allemaal is. Doel is de realiteit te herkennen en te accepteren zoals ze is, zonder deze te kleuren door
                emotie of te reageren vanuit automatisme. Mindfulness is daarmee een levenshouding die ons leert bewuster in
                het leven te staan en daarmee keuzevrijheid geeft in handelen.</
            </p>
        </div>
        <div> <h3>Hoe doen we dit?</h3>
            <p> Via aandachtgerichte, meditatie oefeningen: Eenvoudige bewegingsoefeningen, de bodyscan, ademoefeningen en
                zitmeditatie. Ook is er sprake van informele oefeningen die op elk moment van de dag een plek kunnen
                krijgen; van het met aandacht luisteren naar de ander tot tanden poetsen. Mediteren betekent: een oefening
                die helpt om met meer gemak om te kunnen gaan met alle aspecten van het leven; plezierige en onplezierige
                momenten; momenten die je saai vindt of interessant. Ook word je bewust van de eigen leefstijl. Vaak ook
                van de wijze waarop je tot nu toe met klachten als pijn, stress, werkdruk of verdriet om bent gegaan.
            </p>
        </div>
        <div> <h3> Hoe ziet de les eruit? </h3>
            <p> De nadruk van de training ligt op ‘leren door ervaren’ aan de hand van
                oefeningen, zoals: begeleide aandachtsoefeningen, bewegingsoefeningen, zit -en loopmeditatie. Ook worden er
                oefeningen gedaan om het bewustzijn in het dagelijkse leven te vergroten. Daarnaast is er ruimte voor het
                bespreken en delen van de ervaringen. Hierdoor wordt de opmerkzaamheid verscherpt. In de groep onderzoek je
                je eigen mogelijkheden om bewuster in het leven te staan. Wat wij in onze training bieden is dat we mindfulness
                vooral praktisch toepasbaar willen maken in het dagelijks leven. Wij kiezen bewust voor een ervaringsgerichte
                aanpak, waarbij de zintuigen en het lichaam een belangrijke rol spelen.
            </p>
        </div>
